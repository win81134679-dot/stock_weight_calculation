/**
 * 判斷股票代碼是否為 ETF
 * 規則：代碼在 ETF_CODES 裡，或以 00 開頭且長度 >= 4
 */
export function isETF(code: string): boolean {
  const trimmed = code.trim().toUpperCase()
  if (ETF_CODES.has(trimmed)) return true
  // 大部分台灣 ETF 代碼以 00 開頭
  if (trimmed.startsWith('00') && trimmed.length >= 4) return true
  return false
}
